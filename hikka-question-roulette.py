    @loader.command()
    async def q(self, message):
        """<тема> [количество] - Сгенерировать вопросы"""
        args = utils.get_args_raw(message).split()
        
        if not args:
            await utils.answer(message, self.strings("no_topic"))
            return
            
        topic = args[0].lower()
        count = 1
        
        if len(args) > 1:
            try:
                count = int(args[1])
                if count < 1 or count > 20:
                    count = 1
            except:
                count = 1
                
        if topic not in self.templates:
            await utils.answer(message, f"❌ <b>Тема '{topic}' не найдена!</b>\nСписок тем: .qlist")
            return
            
        questions = []
        for _ in range(count):
            question = self._generate_question(topic)
            questions.append(f"• {question}")
            
        text = f"<b>🎯 Вопросы по теме '{topic}':</b>\n\n"
        text += "\n".join(questions)
        
        await utils.answer(message, text)
        
    @loader.command()
    async def qall(self, message):
        """Случайный вопрос из любой темы"""
        topic = random.choice(list(self.templates.keys()))
        question = self._generate_question(topic)
        
        text = f"<b>🎲 Случайный вопрос:</b>\n<b>Тема:</b> {topic}\n\n• {question}"
        await utils.answer(message, text)
        
    @loader.command()
    async def qlist(self, message):
        """Список всех доступных тем"""
        topics = list(self.templates.keys())
        
        text = "<b>📋 Доступные темы:</b>\n\n"
        
        # Красивое оформление с эмодзи
        emoji_list = ["1️⃣", "2️⃣", "3️⃣", "4️⃣", "5️⃣", "6️⃣", "7️⃣", "8️⃣", "9️⃣", "🔟", "1️⃣1️⃣", "1️⃣2️⃣", "1️⃣3️⃣"]
        
        for i, topic in enumerate(topics):
            emoji = emoji_list[i] if i < len(emoji_list) else "•"
            text += f"{emoji} <b>{topic.capitalize()}</b>\n"
            
        text += "\n<i>Используй: .q [тема] [количество]</i>"
        
        await utils.answer(message, text)
        
    def _generate_question(self, topic):
        """Генерация вопроса по теме"""
        template = self.templates[topic]
        
        start = random.choice(template["start"])
        middle = random.choice(template["middle"])
        end = random.choice(template["end"])
        
        # Собираем вопрос
        if random.choice([True, False]):  # Иногда меняем порядок
            question = f"{start} {middle} {end}"
        else:
            question = f"{start} {end[:-1]} {middle}?"
            
        # Делаем первую букву заглавной
        question = question[0].upper() + question[1:]
        
        # Убираем лишние пробелы
        question = " ".join(question.split())
        
        return question
